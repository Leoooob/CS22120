<h2>About</h2>

<p>
	Welcome to RPSRview. Here you will be able to view reserves and species details about the plants 
	you encounter in your travels. RPSRview is in part of our Android app RPSRrec, which allows you 
	to record your findings on your Android device, complete with GPS locating and automatic updating 
	to the server which stores all of this data for you.
</p>
<p>
	RPSRview is a user friendly and competent website which will display every reserve visited with 
	the RPSRrec Android app, and the details of each visit, including each species found.
</p>

<h3>How to use RPSRview</h3>
<h4>Regular users</h4>
<p>
	The site is very easy to use. On loading the site, you will see the list of reserves that have been
	visited. Clicking the name of a reserve will take you to a page with a list of species inside that 
	reserve. This list will be ordered by its latin name, but you can choose to sort it by the time and
	date it was recorded. Clicking on a specimen's name will take you to a page that will show you its 
	details.
</p>

<h4>Administrators</h4>
<p>
	You can login as an administrator by clicking on the 'Admin login' link at the top of the page and
	entering the password. Once logged in, administrators have the ability to create, edit and delete
	reserves and species.
</p>
<p>
	To create a reserve, click on the 'Add reserve' button on the Reserves page and enter its 
	details on the next page. To edit a reserve, select a reserve and then click on the 'Edit reserve' 
	button, and change any details you wish to edit on the next page. To delete a reserve, simply
	select a reserve and click on the 'Delete reserve' button.
</p>

